                        <h2>Mängujuhi juhend</h2>
                        
                        <p>Järgnev juhend õpetab kasutama “Mängujuhi” lehte.</p>

                        <p>Mängujuht alustab mängu mängijate registreerimisega. Mängu jaoks tuleb sisestada mängija nimi ja mäng annab ise koodi. Kui mängijad on lisatud, peab mängujuht ütlema mängijatele nende koodi. Igal mängijal on oma kood. Kood tuleb õpilasel sisestada oma töövahendisse(telefon, arvuti).</p>

                        <p>Samal ajal, kui õpilased mängu logivad, hakkab mängujuht valima küsimusi. Küsimused on jaotatud kolme rühma, kust mängujuht valib kokku üheksa küsimust. Peale "Edasi" nupule vajutamist hakkab mäng peale.</p>

                        <p>Mängujuhile kuvatakse küsimused ja vastused, kuid ta ei saa ise vastata. Kui mängujuht arvab, et vastamisaeg on läbi või kõik on vastanud, lõpetab Ta vooru. Peale mida kuvatakse tulemused. Kui mängujuht soovib võib arutada vastused läbi ja siis liikuda järgmise küsimuse juurde. Mängujuht lõpetab voor, millega algab järgmine. Selline liikumine kestab, kuni kõigile valitud küsimustele on vastatud.</p>

                        <p>Mängu lõpus näeb mängujuht kõigi mängijate vastuseid valitud küsimustele. Soovi korral võib tulemused kopeerida. <bold>Kui mängujuht lõpetab tulemuste vaatamise, kaovad mängijate vastused ja sama mängu juurde ei sa enam tagasi.</bold></p>
