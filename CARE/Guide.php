                        <h2>Mängija juhend</h2>
                        <p>Järgnev juhend õpetab kasutama “Mängija” lehte.</p>
                        
                        <p>Mängija saab mängujuhilt parooli, kuue kohaline arv, mis tuleb sisestada oma töövahendisse(telefon, arvuti).</p>
                        
                        <p>Mäng algab, kui kõik mängijad on sisse saanud. </p>
                        
                        <p>Mängija ülesanne on hoolikalt läbi lugeda küsimused ja vastata neile. Vastamisaja otsustab mängujuht. Mängujuht ei pea ootama, kuni kõik mängijad on vastanud.</p>
                        
                        <p>Kui mängija vastas õige vastusega, saab ta ühe punkti/tähe. </p>
                        
                        <p>Mängu võidab enim punkte saanud mängija</p>
                        
                        <p>Kui mängija vastab õigesti kõik üheksa küsimust, saab ta teada mängu vastuse.</p>
